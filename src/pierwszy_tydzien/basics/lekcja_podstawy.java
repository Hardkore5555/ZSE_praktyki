package pierwszy_tydzien.basics;

public class lekcja_podstawy {
// to jest metoda główna
    public static void main(String[] args) {
// liczby całkowite
        int A = 1;
        int B = -32;
        int C =  77;
// dodawanie zmiennych
int sum = A + B + C;
System.out.println("Dodawanie wynik: "+ sum);
// odejmowanie zmiennych
sum = A - B - C;
System.out.println("Odejmowanie wynik: "+ sum);
// mnożenie zmiennych
sum = A * B * C;
System.out.println("Mnożenie wynik: "+ sum);
//dzielenie zmiennych
sum = B / 2;
System.out.println("Dzielenie zmiennej B przez 2: "+ sum);
    }

}
